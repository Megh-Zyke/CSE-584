    static = (STATIC_QUESTIONS * 3)[:700]
    slow = (SLOW_MOVING_QUESTIONS * 3)[:700]
    volatile = (VOLATILE_QUESTIONS * 3)[:700]

    rows = []
    for i, q in enumerate(static):
        rows.append({
            "Record_ID": f"EVAL-S-{i+1:04d}",
            "Question": q,
            "Temporal_Category": "Static",
            "Expected_TTL": "Never expires",
            "Reasoning_Notes": "Factual, historical, or scientific — answer does not change"
        })
    for i, q in enumerate(slow):
        rows.append({
            "Record_ID": f"EVAL-SM-{i+1:04d}",
            "Question": q,
            "Temporal_Category": "Slow-Moving",
            "Expected_TTL": "30 days",
            "Reasoning_Notes": "Changes occasionally — versions, policies, recommendations"
        })
    for i, q in enumerate(volatile):
        rows.append({
            "Record_ID": f"EVAL-V-{i+1:04d}",
            "Question": q,
            "Temporal_Category": "Volatile",
            "Expected_TTL": "5 minutes",
            "Reasoning_Notes": "Real-time data — prices, weather, scores, breaking news"
        })

    random.shuffle(rows)
    df = pd.DataFrame(rows)

    os.makedirs("datasets", exist_ok=True)
    filename = "datasets/evaluation_dataset.xlsx"
